// Receber dois números inteiros em seguida informar a soma, subtração, multiplicação e divisão dos dois
let valorA = parseInt(prompt("Informe o primeiro valor"))
let valorB = parseInt(prompt("Informe o segundo valor"))
let soma = valorA+valorB
let subtracao = valorA-valorB
let multiplicacao = valorA*valorB
let divisao = valorA/valorB
alert(`${valorA} + ${valorB} = ${soma} 
${valorA} - ${valorB} = ${subtracao} 
${valorA} x ${valorB} = ${multiplicacao}
${valorA} / ${valorB} = ${divisao} `)